<p>5.2.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Совершеннолетний пациент вправе определить лиц, которым следует сообщать информацию о состоянии его здоровья, либо запретить ее сообщение определенным лицам.</p>



