<p>1.3.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Настоящие правила размещены на бумажном носителе в холле Стоматологического центра на информационном стенде, в электронном виде &#8212; на официальном сайте ООО «Ю-КЛИНИК» по адресу: https://u-clinic.by.</p>



<p>1.4.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Внутренний распорядок для пациентов Стоматологического центра – это регламент (порядок) выполнения профессиональной деятельности работниками Стоматологического центра, обеспечивающий получение пациентом платных медицинских услуг в области стоматологии надлежащего качества, а также права и обязанности пациента при оказании платных медицинских услуг в области стоматологии.</p>



<p>1.5.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Правила обязательны для исполнения всеми пациентами, обращающимися за оказанием платных медицинских услуг в области стоматологии, а также&nbsp;работниками Стоматологического центра. Пациент должен ознакомиться с Правилами самостоятельно устно до обращения за услугой (заключения договора на оказание платных медицинских услуг).&nbsp;Факт ознакомления с настоящими Правилами подтверждается пациентом (его законным представителем) путем присоединения к Публичному договору-оферты на оказание платных медицинских услуг, подтверждается подписью пациента (его законного представителя) и вносится в медицинские документы.</p>



<p>1.6.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;В Стоматологическом центре все медицинские услуги оказываются пациентам на платной плановой основе по предварительной записи по видам медицинской деятельности, которые оговорены в лицензии, выданной для ООО «Ю-КЛИНИК» Министерством здравоохранения Республики Беларусь.</p>



