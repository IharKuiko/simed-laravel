<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;При наличии документов, подтверждающих сведения, изложенные в обращении, их должны быть приложены. К обращениям, подаваемых представителями, прилагаются документы, подтверждающие их полномочия.&nbsp;</p>



