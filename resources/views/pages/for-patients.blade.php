<p>2.11.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;При обращении пациента в Стоматологический центр без предварительной записи платные медицинские услуги в области стоматологии ему оказываются только в случае отсутствия записи на желаемое время со стороны других пациентов.</p>



