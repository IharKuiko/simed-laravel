<p>4.2.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Пациент также может подать электронное обращение в адрес Стоматологического центра с использованием государственной единой (интегрированной) республиканской информационной системы учета и обработки обращений граждан и юридических лиц.</p>



<p>4.3.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Обращения подаются и рассматриваются Стоматологическим центром в соответствии с Законом Республики Беларусь «Об обращениях граждан и юридических лиц».</p>



<p>4.4.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Пациент (его законный представитель) может обращаться с письменным обращением к руководителю Стоматологического центра. Ответ пациенту на обращение предоставляется в письменном виде в сроки, установленные законодательством Республики Беларусь.</p>



