<p>1.8.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;При состояниях, требующих срочного медицинского вмешательства, пациент должен обращаться в организации здравоохранения, оказывающие срочную (неотложную) медицинскую помощь.</p>



<p>1.9.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Услуги оказываются Стоматологическим центром по месту его нахождения &#8212; г.Гомель,&nbsp;ул.Каменщикова, дом 36, помещение 4.</p>



<p>1.10.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Плановая платная медицинская услуга может быть оказана исключительно в случае обоюдного согласия сторон &#8212; пациента и Стоматологического центра.</p>



<p>1.11.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Перечень платных медицинских услуг, оказываемых&nbsp;Стоматологическим центром&nbsp;соответствует Прейскуранту, который утверждается приказом директором ООО «Ю-КЛИНИК» и размещается в доступных для ознакомления местах, в регистратуре Стоматологического центра, на информационном стенде, официальном сайте ООО «Ю-КЛИНИК».</p>



