<p>5.4.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Информация, содержащаяся в медицинской документации, составляет врачебную тайну и может предоставляться без согласия пациента (его законного представителя) только по основаниям, предусмотренным законодательными актами.</p>



